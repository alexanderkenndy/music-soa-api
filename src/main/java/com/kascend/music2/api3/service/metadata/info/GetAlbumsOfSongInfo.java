package com.kascend.music2.api3.service.metadata.info;

public class GetAlbumsOfSongInfo extends PageInfo {
	
	private String song;
	private long songid;
	private String artist;
	private int albumartflag;
	private int bioflag;
	private int albumartsize;
	public String getSong() {
		return song;
	}
	public void setSong(String song) {
		this.song = song;
	}
	public long getSongid() {
		return songid;
	}
	public void setSongid(long songid) {
		this.songid = songid;
	}
	public String getArtist() {
		return artist;
	}
	public void setArtist(String artist) {
		this.artist = artist;
	}
	public int getAlbumartflag() {
		return albumartflag;
	}
	public void setAlbumartflag(int albumartflag) {
		this.albumartflag = albumartflag;
	}
	public int getBioflag() {
		return bioflag;
	}
	public void setBioflag(int bioflag) {
		this.bioflag = bioflag;
	}
	public int getAlbumartsize() {
		return albumartsize;
	}
	public void setAlbumartsize(int albumartsize) {
		this.albumartsize = albumartsize;
	}
	
	

}
