package com.kascend.music2.api3.service.metadata.info;

import java.util.List;

public class RecommendalbumInfo extends PageInfo {
	private String album;
	private String artist;
	private long albumid;
	private int albumartflag;
	private int albumartsize;
	private int bioflag;
	
	private List<Long> artistIdList;
	
	
	public List<Long> getArtistIdList() {
		return artistIdList;
	}
	public void setArtistIdList(List<Long> artistIdList) {
		this.artistIdList = artistIdList;
	}
	public String getAlbum() {
		return album;
	}
	public void setAlbum(String album) {
		this.album = album;
	}
	public String getArtist() {
		return artist;
	}
	public void setArtist(String artist) {
		this.artist = artist;
	}
	public long getAlbumid() {
		return albumid;
	}
	public void setAlbumid(long albumid) {
		this.albumid = albumid;
	}
	public int getAlbumartflag() {
		return albumartflag;
	}
	public void setAlbumartflag(int albumartflag) {
		this.albumartflag = albumartflag;
	}
	public int getAlbumartsize() {
		return albumartsize;
	}
	public void setAlbumartsize(int albumartsize) {
		this.albumartsize = albumartsize;
	}
	public int getBioflag() {
		return bioflag;
	}
	public void setBioflag(int bioflag) {
		this.bioflag = bioflag;
	}
	
	

}
