public class GetUserArtistInfo extends PageInfo {
	private long targetuid;
	
	private List<Long> artistIdList;
	
	private int userPlaylistDataFrom;
	
	private long playlistid;
	

	public long getPlaylistid() {
		return playlistid;
	}

	public void setPlaylistid(long playlistid) {
		this.playlistid = playlistid;
	}

	public int getUserPlaylistDataFrom() {
		return userPlaylistDataFrom;
	}

	public void setUserPlaylistDataFrom(int userPlaylistDataFrom) {
		this.userPlaylistDataFrom = userPlaylistDataFrom;
	}

	public List<Long> getArtistIdList() {
		return artistIdList;
	}

	public void setArtistIdList(List<Long> artistIdList) {
		this.artistIdList = artistIdList;
	}

	public long getTargetuid() {
		return targetuid;
	}

	public void setTargetuid(long targetuid) {
		this.targetuid = targetuid;
	}
	
	
}
