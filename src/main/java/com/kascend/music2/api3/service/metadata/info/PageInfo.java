package com.kascend.music2.api3.service.metadata.info;

import com.kascend.music2.api3.service.info.MusicBaseInfo;

public class PageInfo extends MusicBaseInfo {
	
	private int page=1;
	private int pagecount=50;
	private int allcountflag;
	private int start;
	
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getPagecount() {
		return pagecount;
	}
	public void setPagecount(int pagecount) {
		this.pagecount = pagecount;
	}
	public int getAllcountflag() {
		return allcountflag;
	}
	public void setAllcountflag(int allcountflag) {
		this.allcountflag = allcountflag;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}

}
