package com.kascend.music2.api3.entity;

public class UserPopularityLog extends BaseData {
	private long id;
	private long uid;
	private int popularityType;
	private int popularity;
	private int logTime;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getUid() {
		return uid;
	}
	public void setUid(long uid) {
		this.uid = uid;
	}
	public int getPopularityType() {
		return popularityType;
	}
	public void setPopularityType(int popularityType) {
		this.popularityType = popularityType;
	}
	public int getPopularity() {
		return popularity;
	}
	public void setPopularity(int popularity) {
		this.popularity = popularity;
	}
	public int getLogTime() {
		return logTime;
	}
	public void setLogTime(int logTime) {
		this.logTime = logTime;
	}
	
}
