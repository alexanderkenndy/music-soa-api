package com.kascend.music2.api3.entity;

public class UserMessage extends BaseData {
	
	private long uid;
	private String nicNname;	
	private String headIcon;	
	private Long messagetId;	
	private String messageContent;	
	private Long createTime;
	private int status;
	private int isReply;
	
	private long fromUid;
	
	private long replyMessageId;

	
	public long getUid() {
		return uid;
	}
	public void setUid(long uid) {
		this.uid = uid;
	}
	public String getNicNname() {
		return nicNname;
	}
	public void setNicNname(String nicNname) {
		this.nicNname = nicNname;
	}
	public String getHeadIcon() {
		return headIcon;
	}
	public void setHeadIcon(String headIcon) {
		this.headIcon = headIcon;
	}
	public Long getMessagetId() {
		return messagetId;
	}
	public void setMessagetId(Long messagetId) {
		this.messagetId = messagetId;
	}
	
	public String getMessageContent() {
		return messageContent;
	}
	public void setMessageContent(String messageContent) {
		this.messageContent = messageContent;
	}
	
	
	public Long getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Long createTime) {
		this.createTime = createTime;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public int getIsReply() {
		return isReply;
	}
	public void setIsReply(int isReply) {
		this.isReply = isReply;
	}
	public long getFromUid() {
		return fromUid;
	}
	public void setFromUid(long fromUid) {
		this.fromUid = fromUid;
	}
	public long getReplyMessageId() {
		return replyMessageId;
	}
	public void setReplyMessageId(long replyMessageId) {
		this.replyMessageId = replyMessageId;
	}
	
	
}
